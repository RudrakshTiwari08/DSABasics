#include <iostream>
using namespace std;
int main()
{
    int n;

    cout<<"enter the number of the week btw 1-7";
    cin>>n;

    if (n==1)
    {
        cout<<"MONDAY";
    }
    if (n==2)
    {
        cout<<"TUESDAY";
    }
    if (n==3)
    {
        cout<<"WEDNESDAY";
    }
    if (n==4)
    {
        cout<<"THRUSDAY";
    }
    if (n==5)
    {
        cout<<"FRIDAY";
    }
    if(n==6)
    {
        cout<<"SATURDAY";
    }
    if (n==7)
    {
        cout<<"SUNDAY";
    }
}
    
